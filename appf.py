def summary_text(metric, score):

    percent = int(score * 20)

    if score <= 1:
        level = "ระดับต่ำมาก"
    elif score <= 2:
        level = "ระดับต่ำ"
    elif score <= 3:
        level = "ระดับปานกลาง"
    elif score <= 4:
        level = "ระดับดี"
    else:
        level = "ระดับสูง"

    return f"{metric} มีคะแนน {score} อยู่ใน{level} คิดเป็น {percent}% ของ Industry 4.0"

