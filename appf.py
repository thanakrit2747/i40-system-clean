@st.cache_data
def extract_company_name(file):
    try:
        raw = pd.read_excel(file, sheet_name="Summary", header=None)
        raw = raw.astype(str)

        keywords = ["บริษัท", "Company", "Co.", "Ltd"]

        for row in raw.values:
            for cell in row:
                if any(k in cell for k in keywords):
                    return cell.strip()

        return "ไม่พบชื่อบริษัท"

    except:
        return "อ่านชื่อบริษัทไม่ได้"

@st.cache_data
def extract_data(file):
    try:
        raw = pd.read_excel(file, sheet_name="Summary", header=None)

        metrics = [
            "Production Automation","Enterprise Automation","Facility Automation",
            "Production Network","Enterprise Network","Facility Network",
            "Smart Production","Smart Enterprise","Smart Facility",
            "Internal Integration","External Integration","Market Analysis",
            "Product Life Cycle","Top-down Management","i4.0 Strategy",
            "Inter-company Collaboration","Workforce Learning"
        ]

        scores = []
        raw_values = raw.values.astype(str)

        for metric in metrics:
            found = False

            for i in range(len(raw_values)):
                for j in range(len(raw_values[i])):
                    if metric in raw_values[i][j]:

                        for k in range(1,6):
                            try:
                                val = float(raw.iloc[i, j+k])
                                scores.append(val)
                                found = True
                                break
                            except:
                                continue

                        if found:
                            break
                if found:
                    break

            if not found:
                scores.append(None)

        df = pd.DataFrame({"Metric": metrics, "Score": scores})
        df = df.dropna()
        df["Score"] = pd.to_numeric(df["Score"], errors="coerce")
        df = df.dropna()
        df["Score"] = df["Score"].round(1)

        return df

    except:
        return pd.DataFrame(columns=["Metric","Score"])

def classify_size(df):
    avg = df["Score"].mean()
    if avg < 2:
        return "ขนาดเล็ก"
    elif avg < 3:
        return "ขนาดกลาง"
    else:
        return "ขนาดใหญ่"

def find_weakness(df):
    return df[df["Score"] <= 2].sort_values(by="Score").head(5)

